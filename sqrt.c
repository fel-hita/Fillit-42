#include "fillit.h"

int			ft_sqrt(t_tetris *t)
{
	int n;
	int i;

	while (t->next)
		t = t->next;
	i = 1;
	n = (t->c - 64) * 4;
	while (i < n / 2)
	{
		if (i * i >= n)
			return (i);
		i++;
	}
	return (2);
}
